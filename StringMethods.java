class StringMethods
 {
    public static void main(String[] args)
     {
    String txt = "Hello World";
System.out.println(txt.toUpperCase());
System.out.println(txt.toLowerCase());
System.out.println(txt.startsWith("H"));
System.out.println(txt.endsWith("H"));
System.out.println(txt.concat(" Java"));
System.out.println(txt.indexOf("l"));
System.out.println(txt.lastIndexOf("l"));
System.out.println(txt.isEmpty());
System.out.println(txt.equalsIgnoreCase("hello world"));
System.out.println(txt.equals("hello world"));
}
}
